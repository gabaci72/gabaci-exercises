for (let counter = 0; counter < 4; counter++) {
  console.log(counter);
}
// Output: 0, 1, 2, 3
// Explaination: The for loop starts with counter = 0, and it continues as long as counter is less than 4. Inside the loop, the current value of counter is logged to the console. After each iteration, counter is incremented by 1.
