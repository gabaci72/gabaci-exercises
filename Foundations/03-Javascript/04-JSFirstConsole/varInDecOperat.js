// The Increment and Decrement Operator //

var counter = 0;

console.log('Initial counter value:', counter);

counter++; // Increment counter by 1
console.log('Incremented counter value:', counter); counter++; // Increment counter by 1
console.log('Incremented counter value:', counter); counter++; // Increment counter by 1
console.log('Incremented counter value:', counter);     // Increment counter by 1   and increment counter by                   1                                                                
console.log("Final counter value:", counter);       

// Increment Operator //
let a = 10;
a++;
console.log(a); // Output: 11
console.log("----------------------");
let a = 10;
a++;
console.log(a); // Output: 11

// Decrement Operator //
let b = 20;
b--;
console.log(b); // Output: 19

